
// 'for-loop' version
for (let i = 0; i < 5; i++) {
    console.log('Hello World!');
}

// 'while-loop' version
//
// control variable definition
// while(condition){
//   statement...
//   increment/decrement of control variable..   
// }
let i = 0;
while (i < 5) {
    console.log('Hello World!');
    i++;
}