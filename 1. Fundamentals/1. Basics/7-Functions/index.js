// Function basically a set of statement that performs a task or calculates a value

// Function definition
// 'name' is called parameter
function greet(name){
    console.log("Hello " + name)
}   // When define a function there is no need to attact semicolon at the end

// Calling the function
// 'Yosendra' is called argument
greet("Yosendra");  // Output: Hello Yosendra
greet("John");      // Output: Hello John