// Javascript is Dynamic-Type language
// Two Kinds of Programming Language
//  -> Static typed. 
//      Once defined, cannot be changed to another type.
//      string name = "Yosendra"
//  -> Dynamic typed
//      Once defined, still can be changed to another type.
//      let name = "Yosendra"

let name = "Yosendra";      // String literal
let number = 30;            // Number literal
let isApproved = true;      // Boolean literal
let firstName = undefined;  // Undefined
let selectedColor = null;   // Null

console.log(typeof name);   // Output: string
name = 1;                   // Value type change to number
console.log(typeof name);   // Output: number